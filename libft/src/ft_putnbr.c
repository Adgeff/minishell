#include <unistd.h>

void		ft_putnbr(int n)
{
	char	c;

	if (n >= 10 || n <= -10)
		ft_putnbr(n / 10);
	else if (n < 0)
		write(1, "-", 1);
	c = n % 10 * ((n < 0) ? -1 : 1) + '0';
	write(1, &c, 1);
}
