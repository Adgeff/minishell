#include <string.h>

int			ft_strnequ(const char *s1, const char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] && i < n)
		i++;
	return (i == n || s1[i] == s2[i] ? 1 : 0);
}
